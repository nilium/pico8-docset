--- Table functions.
-- @module Tables

---------
-- Adds the v to the table t
-- @function add
-- @tab t The table to append to.
-- @param v The value to add.

----------
-- Deletes v from the table t
-- @function del
-- @tab t The table to remove from.
-- @param v The value to remove.


----------
-- Iterates over t, returning all entries.
-- @function pairs
-- @tab t The table to iterate over.

----------
-- Iterates over t, returning all consecutive entries.
-- @function all
-- @tab t The table to iterate over.

----------
-- Iterate t calling f for every value
-- @function foreach
-- @tab t The table to iterate over.
-- @func f The iterator function.
